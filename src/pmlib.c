int pbmwrite(char* path, int *mat, int row, int col){
    FILE *fd = fopen(path,"w");
    //open file descriptor
    if(fd==NULL){
        fclose(fd);
        return -1;
    }

    //write the image format
    fprintf(fd, "P1\n");

    
    //Write a comment
    fprintf(fd, "#Autogenerated by MPL\n");

    //Write the file size
    fprintf(fd, "%d %d\n", row, col);
   
    //Write the actual bits in
    int count = 0;
    while(count/col < row){
        while(count%col < col-1){
            fprintf(fd,"%d ",*mat);
        }
        if(count%col==0){
            fprintf(fd,"%d\n",*mat);
        }
        mat++;
        count++;
		
        if(count==row*col){
			fclose(fd);
			return 0;
		}
    }

	fclose(fd);
	return -2;	
    
}

